function somar(a, b) {
    return a + b;
}

function subtracao(a, b){
    return a - b;
}

function multiplicacao(a, b) {
    return a * b;
}

function divisao(a, b) {
    return a / b;
}

let resultSoma = somar(5, 3);
console.log('Soma: ' + resultSoma);

let resulSub = subtracao(10, 7);
console.log('Subtração: ' + resulSub);

let resultMult = multiplicacao(4, 6);
console.log('Multiplicação: ' + resultMult);

let resultDiv = divisao(15, 3);
console.log('Divisão: ' + resultDiv);


